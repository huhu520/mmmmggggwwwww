package com.mgw.member.ottoEvent;


/**
 * app可以更新  打开更新activity
 * @author huyan
 *备注：采用eventbus事件订阅总线框架
 */
public class AppShouldUpdateEvent {
	
	

	private boolean  isCanUpdate;
	private String  fv_exp;
	public String getFv_exp() {
		return fv_exp;
	}

	public void setFv_exp(String fv_exp) {
		this.fv_exp = fv_exp;
	}

	private String FVersion_Name;
	private String  FVersion_FileURL;

	
	
	public String getFVersion_Name() {
		return FVersion_Name;
	}

	public void setFVersion_Name(String fVersion_Name) {
		FVersion_Name = fVersion_Name;
	}

	public String getFVersion_FileURL() {
		return FVersion_FileURL;
	}

	public void setFVersion_FileURL(String fVersion_FileURL) {
		FVersion_FileURL = fVersion_FileURL;
	}

	public boolean isCanUpdate() {
		return isCanUpdate;
	}

	public void setCanUpdate(boolean isCanUpdate) {
		this.isCanUpdate = isCanUpdate;
	}

	


	
	
}
